Mat4UniformBufferData::Mat4UniformBufferData(int inMat4Count) {
	mMat4Count = inMat4Count;
	mbNeedSyncData = true;
	mResource = nullptr;
}
void Mat4UniformBufferData::SetMat4(int inIndex, void* inMat4) {
	int offset = inIndex * 16;
	memcpy(mData + offset, inMat4, sizeof(float) * 16);
	mbNeedSyncData = true;
}
void Mat4UniformBufferData::UpdateGPUData() {
	if (!mbNeedSyncData) {
		return;
	}
	if (mResource == nullptr) {
		mResource = GenConstantBuffer(sizeof(mData));
	}
	mbNeedSyncData = false;
	UpdateConstantBuffer(mResource, mData, sizeof(mData));
}
